       janelFunc.atualizaQntComprada(quantidade);
       janelFunc.atualizaValorTotal(this.pVo.getValorUnitario(), quantidade);
       janelFunc.atualizaCarrinho("" + janelFunc.getQntComprada(), "" + ("R$ " + String.valueOf((janelFunc.getValorTotal()) + "0").replace(".", ",")));
