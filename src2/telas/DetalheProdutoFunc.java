    private void abreImagem(){
        Connection connection;
        try {
            connection = ConectaBD.getConnection();
            PreparedStatement statement = connection.prepareStatement("SELECT * FROM Imagem WHERE codigoImagem = ?");
            statement.setInt(1, pVo.getCodigoImagem());
            ResultSet resultSet = statement.executeQuery();
            resultSet.next();
            Blob blob = resultSet.getBlob("imagem");
            resultSet.close();
            statement.close();
            connection.close();
            ImageIcon imageIcon = new ImageIcon(blob.getBytes(1, (int)blob.length()));
            this.labelImagem.setIcon(imageIcon);
        } catch (SQLException ex) {
            Logger.getLogger(SalvaImagens.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Não foi possível resgastar a imagem!\nSQLException", "Erro", JOptionPane.ERROR_MESSAGE);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(SalvaImagens.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Não foi possível resgastar a imagem!\nClassNotFoundException", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    } 

